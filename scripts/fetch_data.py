    # ── 月次ヒートマップキャッシュ ──
    print("集計: heatmap_monthly")
    months = []
    for i in range(11, -1, -1):
        total_months = now_jst.month - 1 - i
        if total_months < 0:
            year_offset = -((-total_months + 11) // 12)
